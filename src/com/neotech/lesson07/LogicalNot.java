package com.neotech.lesson07;

public class LogicalNot {

	public static void main(String[] args) {
		boolean b1 = true;
		System.out.println(b1);
		boolean b2 = !false;
		System.out.println(b2);
		// ! = is the opposite of.
		
		boolean cold = true;
		if (!cold)
		{
			System.out.println("It is cold!");
		}
				

	}

}
